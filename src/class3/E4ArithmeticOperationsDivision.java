package class3;

public class E4ArithmeticOperationsDivision {
    public static void main(String[] args) {

        int number1=10;
        int number2=5;
        int divison=number1/number2;
        System.out.println(number1/number2);
        System.out.println(10/5);
        System.out.println(divison);
        System.out.println(11/5);// this does not show the remainder because int can not hold decimal value
        System.out.println(11.0/5.0);// this showed the reminader because the data type was double
        int a=10;
        int b=4;
        double a1=10;
        double a2=4;

        System.out.println(a/b);
        System.out.println(a1/a2);



    }
}
