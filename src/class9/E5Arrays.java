package class9;

public class E5Arrays {
    public static void main(String[] args) {

        String [] names={"Qadeer","Khatira","Zarmina","Ghul","Said"};
        for (int i = 0; i < names.length; i++) {
            System.out.print(names [i]+" ");

        }
        System.out.println();
        int j=0;
        while (j< names.length){
            System.out.print(names [j]+" ");
            j++;
        }
    }
}
