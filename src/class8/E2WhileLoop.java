package class8;

public class E2WhileLoop {

    public static void main(String[] args) {
        // to print all whole numbers from 15 to 30
        // now lets create another variable and store instead of start and end

        int start=15;
        int end=30;
        int number=start;
        while (number<=end){
            System.out.print(number+" ");
            number++;
        }
    }
}
