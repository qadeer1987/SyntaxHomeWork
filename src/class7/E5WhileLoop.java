public class E5WhileLoop {
    public static void main(String[] args) {
        // print numbers 3 to 11 using a while loop
        // print these numbers 2 4 6 8 10 12 14 using while loop
        int counter=2;
        while (counter<15){

            System.out.println(counter);
            // below line will increase the value of hte counter by 2

            counter+=2;// this line can be written as counter=counter+2 or counter++ counter++

        }

    }
}
