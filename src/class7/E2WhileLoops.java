package class7;

public class E2WhileLoops {

    public static void main(String[] args) {

        //"++" is an increment operator
        int number=0;
        number++;
        number++;
        System.out.println(number);

        // "--" is a decrement operator
        int num2=10;
        num2--;
        System.out.println(num2);

    }
}
