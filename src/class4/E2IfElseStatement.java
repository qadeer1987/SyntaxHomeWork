package class4;

public class E2IfElseStatement {
    public static void main(String[] args) {
        // String is non-primitive data type, so we can't use the "==" sign
        String name="Qadeer";

        if (name.equals("Mike")){
            System.out.println("I like swimming.");
        }else {
            System.out.println("I like programming");
        }
    }
}
